const {
  Document, Packer, Paragraph, TextRun, Table, TableRow, TableCell,
  HeadingLevel, AlignmentType, BorderStyle, WidthType, ShadingType
} = require('docx');
const fs = require('fs');

// Read parsed data from Python script output
const data = JSON.parse(fs.readFileSync('parsed_data.json', 'utf8'));
const { metadata, stats, summary } = data;

// Helper functions
const border = { style: BorderStyle.SINGLE, size: 1, color: 'CCCCCC' };
const borders = { top: border, bottom: border, left: border, right: border };
const cellMargins = { top: 80, bottom: 80, left: 120, right: 120 };

function headerCell(text) {
  return new TableCell({
    borders,
    shading: { fill: '1F3864', type: ShadingType.CLEAR },
    margins: cellMargins,
    children: [new Paragraph({
      alignment: AlignmentType.CENTER,
      children: [new TextRun({ text, bold: true, color: 'FFFFFF', size: 20, font: 'Arial' })]
    })]
  });
}

function dataCell(text, shade = 'FFFFFF', color = '000000', bold = false) {
  return new TableCell({
    borders,
    shading: { fill: shade, type: ShadingType.CLEAR },
    margins: cellMargins,
    children: [new Paragraph({
      children: [new TextRun({ text: String(text), bold, color, size: 18, font: 'Arial' })]
    })]
  });
}

function passRateColor(rate) {
  if (rate >= 90) return 'C6EFCE';  // green
  if (rate >= 70) return 'FFEB9C';  // yellow
  return 'FFC7CE';                   // red
}

// Build probe results table rows
const probeRows = Object.entries(stats).map(([probe, counts]) => {
  const total = counts.pass + counts.fail;
  const rate = total ? ((counts.pass / total) * 100).toFixed(1) : '0.0';
  const shade = passRateColor(parseFloat(rate));
  return new TableRow({
    children: [
      dataCell(probe),
      dataCell(counts.pass, shade),
      dataCell(counts.fail, shade),
      dataCell(rate + '%', shade, '000000', true)
    ]
  });
});

// Build document
const doc = new Document({
  styles: {
    default: { document: { run: { font: 'Arial', size: 24 } } },
    paragraphStyles: [
      { id: 'Heading1', name: 'Heading 1', basedOn: 'Normal', next: 'Normal', quickFormat: true,
        run: { size: 36, bold: true, font: 'Arial', color: '1F3864' },
        paragraph: { spacing: { before: 320, after: 160 }, outlineLevel: 0 } },
      { id: 'Heading2', name: 'Heading 2', basedOn: 'Normal', next: 'Normal', quickFormat: true,
        run: { size: 28, bold: true, font: 'Arial', color: '2E75B6' },
        paragraph: { spacing: { before: 240, after: 120 }, outlineLevel: 1 } },
    ]
  },
  sections: [{
    properties: {
      page: {
        size: { width: 12240, height: 15840 },
        margin: { top: 1440, right: 1440, bottom: 1440, left: 1440 }
      }
    },
    children: [

      // Title
      new Paragraph({
        alignment: AlignmentType.CENTER,
        spacing: { before: 480, after: 120 },
        children: [new TextRun({ text: 'LLM Security Audit Report', bold: true, size: 52, font: 'Arial', color: '1F3864' })]
      }),
      new Paragraph({
        alignment: AlignmentType.CENTER,
        spacing: { after: 480 },
        children: [new TextRun({ text: 'Generated by Garak Automated Vulnerability Scanner', size: 24, color: '595959', font: 'Arial' })]
      }),

      // Executive Summary
      new Paragraph({ heading: HeadingLevel.HEADING_1, children: [new TextRun('Executive Summary')] }),
      new Paragraph({
        spacing: { after: 200 },
        children: [new TextRun({
          text: `This report presents results of an automated LLM security audit using Garak v${metadata.garak_version}. The target model was ${metadata.model_name} via ${metadata.model_type}. A total of ${summary.total_tests} security test cases were executed across ${summary.total_probes} vulnerability categories.`,
          size: 22, font: 'Arial'
        })]
      }),

      // Key Findings Table
      new Paragraph({ heading: HeadingLevel.HEADING_2, children: [new TextRun('Key Findings')] }),
      new Table({
        width: { size: 9360, type: WidthType.DXA },
        columnWidths: [4680, 4680],
        rows: [
          new TableRow({ children: [headerCell('Metric'), headerCell('Value')] }),
          new TableRow({ children: [dataCell('Garak Version'), dataCell(metadata.garak_version)] }),
          new TableRow({ children: [dataCell('Scan Date'), dataCell(metadata.start_time)] }),
          new TableRow({ children: [dataCell('Target Model'), dataCell(metadata.model_name)] }),
          new TableRow({ children: [dataCell('Model Type'), dataCell(metadata.model_type)] }),
          new TableRow({ children: [dataCell('Probe Categories'), dataCell(String(summary.total_probes))] }),
          new TableRow({ children: [dataCell('Total Tests'), dataCell(String(summary.total_tests))] }),
          new TableRow({ children: [dataCell('Total Passed'), dataCell(String(summary.total_pass), 'C6EFCE')] }),
          new TableRow({ children: [dataCell('Total Failed'), dataCell(String(summary.total_fail), 'FFC7CE')] }),
          new TableRow({ children: [dataCell('Overall Pass Rate'), dataCell(summary.overall_pass_rate + '%', passRateColor(summary.overall_pass_rate), '000000', true)] }),
        ]
      }),

      // Probe Results
      new Paragraph({ heading: HeadingLevel.HEADING_2, spacing: { before: 400 }, children: [new TextRun('Detailed Probe Results')] }),
      new Table({
        width: { size: 9360, type: WidthType.DXA },
        columnWidths: [4680, 1560, 1560, 1560],
        rows: [
          new TableRow({ children: [headerCell('Probe Category'), headerCell('Pass'), headerCell('Fail'), headerCell('Pass Rate')] }),
          ...probeRows
        ]
      }),

      // Recommendations
      new Paragraph({ heading: HeadingLevel.HEADING_1, children: [new TextRun('Recommendations')] }),
      new Paragraph({
        spacing: { after: 160 },
        children: [new TextRun({ text: '1. Implement stronger system prompts to resist prompt injection attacks.', size: 22, font: 'Arial' })]
      }),
      new Paragraph({
        spacing: { after: 160 },
        children: [new TextRun({ text: '2. Add content filtering layers to detect and block injection attempts.', size: 22, font: 'Arial' })]
      }),
      new Paragraph({
        spacing: { after: 160 },
        children: [new TextRun({ text: '3. Apply input validation and sanitization before passing user input to the model.', size: 22, font: 'Arial' })]
      }),
      new Paragraph({
        spacing: { after: 160 },
        children: [new TextRun({ text: '4. Schedule weekly automated Garak scans to catch emerging vulnerabilities.', size: 22, font: 'Arial' })]
      }),
      new Paragraph({
        spacing: { after: 160 },
        children: [new TextRun({ text: '5. Integrate Garak into CI/CD pipelines so failed scans block releases.', size: 22, font: 'Arial' })]
      }),
    ]
  }]
});

Packer.toBuffer(doc).then(buffer => {
  fs.writeFileSync('LLM_Security_Audit_Report.docx', buffer);
  console.log('Report generated: LLM_Security_Audit_Report.docx');
});
